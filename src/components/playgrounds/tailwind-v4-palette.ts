export const twOklch = (s: TwStep): Oklch => ({
  mode: 'oklch',
  l: s[0] / 100,
  c: s[1],
  h: s[2],
})

export const twCss = (s: TwStep): string => `oklch(${s[0]}% ${s[1]} ${s[2]})`

export function twFamily(name: string): TwFamily {
  const found = TW_FAMILIES.find((f) => f.name === name)
  if (!found) throw new Error(`unknown family ${name}`)
  return found
}
